    public int multiply(int a, int b) {
        return a * b;
    }

    public int sum(int x, int y) {
        return x + y;
    }

    public int minus(int q, int e) {
        return q - e;
    }

    public int summa(int d, int c) {
        return d + c;
    }

    public int subtracion(int s, int z) {
        return s - z;
    }

    public int div(int a, int b) {
        return a / b;
    }

    public int calPlus(int a, int b) {
        return a + b;
    }

    public int min(int y, int x) {
        return y + x + x;
    }

    public int divAndMinus(int a, int b, int c, int d) {
        return a / b - c - d;
    }

    public int minAndPlus(int a, int b) {
        return a + b + a + b;
    }

    public String plus(String name, String surname) {
        return name + surname;
    }

    public short myfunc(short a, short b, short c) {
        return (short) (a + b * c);
    }
